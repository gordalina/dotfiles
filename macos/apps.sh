# Bartender

# Do not hide menu items when showing hidden ones
defaults write com.surteesstudios.Bartender.plist HideItemsWhenShowingOthers -int 0

# Prevent menu bar items from graying out on non-active monitor
defaults write com.surteesstudios.Bartender.plist HideSecondaryMenuBarItems -int 0

# Show hidden items on hover
defaults write com.surteesstudios.Bartender.plist MouseOverMenuBarTogglesBartender -int 1

# Show for Updates
defaults write com.surteesstudios.Bartender.plist DisableAllSHowForUpdates -int 1
